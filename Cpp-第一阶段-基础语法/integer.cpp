#include<iostream>
using namespace std;
int main()
{
    //短整型
    short a = 10;
    //整型
    int b = 10;
    //长整型
    long c = 10;
    //长长整型
    long long d = 10;
    cout<< "a = "<< a << endl;
    cout<< "b = "<< b << endl;
    cout<< "c = "<< c << endl;
    cout<< "d = "<< d << endl;
    system("pause");
    return 0;
}